const FormDesigner = () => {
  const [schema, setSchema] = useState({
    type: 'object',
    properties: {}
  });
  const [selectedField, setSelectedField] = useState(null);

  //newadd
//   const [previewVisible, setPreviewVisible] = useState(false);

  // ‰øùÂ≠òÂà∞Êú¨Âú∞Â≠òÂÇ®
  const handleSaveSchema = () => {
    localStorage.setItem('form-schema', JSON.stringify(schema));
  };

  // Âä†ËΩΩÂ∑≤Êúâschema
  const handleLoadSchema = (loadedSchema) => {
    Modal.confirm({
      title: 'Á°ÆËÆ§Âä†ËΩΩË°®Âçï',
      content: 'ËøôÂ∞ÜË¶ÜÁõñÂΩìÂâçË°®ÂçïÔºåÊòØÂê¶ÁªßÁª≠Ôºü',
      onOk: () => {
        setSchema(loadedSchema);
        setSelectedField(null);
      }
    });
  };

  const handlePropertyChange = (fieldName, key, value) => {
    const newSchema = {
      ...schema,
      properties: {
        ...schema.properties,
        [fieldName]: {
          ...schema.properties[fieldName],
          [key]: value
        }
      }
    };
    setSchema(newSchema);
  };

  const handleRemoveField = (fieldName) => {
    const newProperties = { ...schema.properties };
    delete newProperties[fieldName];
    
    const newSchema = {
      ...schema,
      properties: newProperties
    };
    
    setSchema(newSchema);
    if (selectedField === fieldName) {
      setSelectedField(null);
    }
  };

  return (
    <DndProvider backend={HTML5Backend}>
      <div style={{ padding: '16px', maxWidth: '1200px', margin: '0 auto' }}>
        <h1 style={{ textAlign: 'center', marginBottom: '16px' }}>Ë°®ÂçïËÆæËÆ°Âô®</h1>
        
        <ActionBar 
          schema={schema} 
          onSaveSchema={handleSaveSchema}
          onLoadSchema={handleLoadSchema}
        />
        
        <Row gutter={16}>
          <Col span={6}>
            <Card title="ÁªÑ‰ª∂ÂàóË°®" style={{ marginBottom: '16px' }}>
              <div style={{ padding: '8px' }}>
                {FORM_COMPONENTS.map(comp => (
                  <ComponentItem key={comp.type} data={comp} />
                ))}
              </div>
            </Card>
          </Col>
          
          <Col span={12}>
            <Card title="ËÆæËÆ°Âå∫Âüü">
              <DesignArea
                schema={schema}
                onSchemaChange={setSchema}
                selectedField={selectedField}
                onSelectField={setSelectedField}
                onRemoveField={handleRemoveField}
                onPropertyChange={handlePropertyChange}
              />
            </Card>
          </Col>
          
          <Col span={6}>
            <Card title="Â±ûÊÄßÈÖçÁΩÆ" style={{ marginBottom: '16px' }}>
              {selectedField ? (
                <PropertyEditor
                  fieldSchema={schema.properties[selectedField]}
                  onPropertyChange={(key, value) => 
                    handlePropertyChange(selectedField, key, value)
                  }
                />
              ) : (
                <div style={{ color: '#999', textAlign: 'center', padding: '16px 0' }}>
                  ËØ∑ÈÄâÊã©Â≠óÊÆµËøõË°åÈÖçÁΩÆ
                </div>
              )}
            </Card>
            
            <SchemaViewer schema={schema} />
          </Col>
        </Row>
      </div>
    </DndProvider>
  );
};

export default FormDesigner;